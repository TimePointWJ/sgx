package com.jtzh.entity;

import java.util.Date;

public class UnionUnion {
    private Integer id;

    private String unionName;

    private String superiorUnionName;

    private Integer superiorUnionId;

    private Date createTime;

    private String delflag;

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getUnionName() {
        return unionName;
    }

    public void setUnionName(String unionName) {
        this.unionName = unionName;
    }

    public String getSuperiorUnionName() {
        return superiorUnionName;
    }

    public void setSuperiorUnionName(String superiorUnionName) {
        this.superiorUnionName = superiorUnionName;
    }

    public Integer getSuperiorUnionId() {
        return superiorUnionId;
    }

    public void setSuperiorUnionId(Integer superiorUnionId) {
        this.superiorUnionId = superiorUnionId;
    }

    public Date getCreateTime() {
        return createTime;
    }

    public void setCreateTime(Date createTime) {
        this.createTime = createTime;
    }

    public String getDelflag() {
        return delflag;
    }

    public void setDelflag(String delflag) {
        this.delflag = delflag;
    }
}